package ar.edu.unlam.pb1;

public class EstadisticasPersonas {
	private int peso;
    private int edad;
    
    public EstadisticasPersonas(int peso, int edad) {
        this.peso = peso;
        this.edad = edad;
    }
    
    public int getPeso() {
        return peso;
    }
    
    public int getEdad() {
        return edad;
    }
}
